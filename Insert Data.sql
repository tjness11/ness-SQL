--fill table Tb_Time, first minute after midnight is minute 0
DECLARE @minutesInDay smallint
SET @minutesInDay=0
WHILE @minutesInDay < 24*60
BEGIN
	INSERT Tb_Time DEFAULT VALUES
	SET @minutesInDay = @minutesInDay + 1
END

--fill table Tb_Date, start date January 1, 2018 is day 0
DECLARE @IsWeekEnd bit
DECLARE @IsHoliday bit
DECLARE @HolidayName varchar(20)
 
DECLARE @Days smallint
SET @Days=0
WHILE @Days < 2000
BEGIN

	SET @IsHoliday = 0
	SET @IsWeekEnd = 0
	SET @HolidayName = 'Business Day'

	IF DATENAME(WEEKDAY, DATEADD(dd, @Days, 'January 1, 2018')) IN ('Saturday','Sunday')
		BEGIN
			SET @IsWeekEnd = 1
			SET @HolidayName = 'Weekend'
		END
	
	IF DATEPART(dd,DATEADD(dd, @Days, 'January 1, 2018')) = 1 AND
		DATENAME(mm,DATEADD(dd, @Days, 'January 1, 2018')) ='January'
		BEGIN
			SET @IsHoliday = 1
			SET @HolidayName = 'New Year'
		END
	ELSE IF DATEPART(dd,DATEADD(dd, @Days, 'January 1, 2018')) = 4 AND
		DATENAME(mm,DATEADD(dd, @Days, 'January 1, 2018')) = 'July'
		BEGIN
			SET @IsHoliday = 1
			SET @HolidayName = 'Independence Day'
		END

	INSERT Tb_Date(IsWeekEnd,IsHoliday,HolidayName) VALUES(@IsWeekEnd,@IsHoliday,@HolidayName)
	SET @Days = @Days + 1
END

